
package Switchcase2;


public class SwitchCase2 {
    
    public static void main(String[]args)
    {
        
        int x=10;
        
        switch(x)
        {
        
            case 2:
                System.out.println("case 2");
                break;
                
            case 10:
                System.out.println("case 10");
                break;
            
            default:
                System.out.println("default");
                break;
        
        
        
        }
    
    
    
    
    }
            
    
}
